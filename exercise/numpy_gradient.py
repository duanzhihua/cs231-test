import numpy as np
x = np.array([1, 2, 4, 7, 11, 16], dtype=np.float)
np.gradient(x)

# =============================================================================
# gradient:(a[i+1]-a[i-1] )/ 2	
# boundary：(a[i]-a[i-1] )/ 1
#
#>>> x = np.array([1, 2, 4, 7, 11, 16], dtype=np.float)
#>>> np.gradient(x)
#    array([1. , 1.5, 2.5, 3.5, 4.5, 5. ])
#  
#    （2-1）/1 ,（4-1）/2，（7-2）/2，（11-4）/2，（16-7）/2，（16-11）/1
# =============================================================================


